package m12_operators_part2;

public class AssignmentOperator {
    public static void main(String[] args) {

        int x= 15;
        System.out.println("x = " + x);

        x = 20;
        System.out.println("x = " + x);
        
        x = x + 5;

        System.out.println("x = " + x);
    }
}
